def publish(daily_md_path: Path, wiki_root: Path) -> int:
    md = daily_md_path.read_text(encoding="utf-8")
    fm, body = parse_frontmatter(md)
    date_str = fm.get("date", _date.today().isoformat())

    sources = parse_sources(body)
    clusters = parse_clusters(body)
    deep_reports = parse_deep_reports(body)

    # Find sibling daily-research-papers.md, if produced by daily_research.py
    papers_by_cluster: dict[str, list[dict[str, Any]]] = {}
    papers_path = daily_md_path.parent / "daily-research-papers.md"
    if papers_path.exists():
        papers_by_cluster = parse_papers_md(papers_path.read_text(encoding="utf-8"))

    pages_dir = wiki_root / "pages"
    pages_dir.mkdir(parents=True, exist_ok=True)

    # Daily entry: pages/autoresearch-{date}.md
    daily_target = pages_dir / f"{daily_page_name(date_str)}.md"
    daily_target.write_text(
        render_daily_entry(
            date_str=date_str, sources=sources, clusters=clusters,
            deep_reports=deep_reports, papers_by_cluster=papers_by_cluster or None,
        ),
        encoding="utf-8",
    )
    print(f"wrote {daily_target}")

    # Papers page: pages/autoresearch-{date}-papers.md (only if we have data)
    if papers_by_cluster:
        papers_target = pages_dir / f"{papers_page_name(date_str)}.md"
        papers_target.write_text(
            render_papers_page(
                date_str=date_str, sources=sources, clusters=clusters,
                deep_reports=deep_reports, papers_by_cluster=papers_by_cluster,
            ),
            encoding="utf-8",
        )
        print(f"wrote {papers_target}")

    # Topic pages: pages/autoresearch-topic-{slug}.md
    deep_by_slug: dict[str, dict[str, Any]] = {}
    for d in deep_reports:
        if d.get("tag_slug"):
            deep_by_slug.setdefault(d["tag_slug"], d)
    for c in clusters:
        topic_slug = slug(c["label"])
        deep = deep_by_slug.get(topic_slug)
        if deep is None:
            for d in deep_reports:
                if d["topic"].strip().lower() == c["label"].strip().lower():
                    deep = d
                    break
        topic_path = pages_dir / f"{topic_page_name(c['label'])}.md"
        page = render_topic_page(
            topic_path=topic_path, label=c["label"], summary=c["summary"],
            keywords=c["keywords"], date_str=date_str, deep=deep,
            papers=papers_by_cluster.get(c["label"], []),
        )
        topic_path.write_text(page, encoding="utf-8")
        print(f"wrote {topic_path}")

    # Index: pages/autoresearch-index.md
    index_target = pages_dir / f"{index_page_name()}.md"
    index_target.write_text(render_index(pages_dir), encoding="utf-8")
    print(f"wrote {index_target}")
    return 0


def main(argv: list[str]) -> int:
    if len(argv) < 3:
        print("usage: wiki_publish.py <daily-research.md> <wiki_root>", file=sys.stderr)
        return 2
    daily_md = Path(argv[1]).expanduser()
    wiki_root = Path(argv[2]).expanduser()
    if not daily_md.exists():
        print(f"daily md not found: {daily_md}", file=sys.stderr)
        return 1
    return publish(daily_md, wiki_root)


if __name__ == "__main__":
    sys.exit(main(sys.argv))
